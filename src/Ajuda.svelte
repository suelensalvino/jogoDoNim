<h1>Como Jogar</h1>
<p>O jogo NIM foi pensado para dois jogadores que podem ser duas pessoas ou uma pessoa e uma máquina. Neste caso, o jogo será executado por uma pessoa e uma máquina que irão retirar as peças dispostas em algumas colunas. 
  Cada jogador realiza jogadas alternadas. Um movimento consiste em retirar algumas peças de uma única coluna; cabe a cada jogador decidir o número de peças a serem  removidas.
  Para eliminar as peças, é necessário deslocar o mouse sobre a quantidade desejada, clicando quando você quiser removê-las, a quantidade selecionada deve mudar de cor indicando que mais uma jogada foi executada. O jogo acaba quando não houver mais peças disponíveis e o jogador que fez a última jogada, vence.
  </p>
